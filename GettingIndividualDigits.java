package gettingindividualdigits;

/**
 *
 * @author kendrabooker
 */
public class GettingIndividualDigits {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        for(int i=1; i<=10; i++){
        
            for(int j=0; j <= 9; j++){
            
                System.out.println(i + "" + j + ", " + i + " + " + j + " = " + " " + (i + j) );
            }
        }
    }
    
}
